function GRID = approximate_cell_centers(GRID)
for n = 1:GRID.nblocks
    x = GRID.gblock(n).x;
    y = GRID.gblock(n).y;
    z = GRID.gblock(n).z;
    i1 = 1:GRID.gblock(n).imax-1;
    i2 = 1:GRID.gblock(n).jmax-1;
    if GRID.gblock(n).kmax > 1
        i3 = 1:GRID.gblock(n).kmax-1;
        GRID.gblock(n).xc = ( x(i1  ,i2  ,i3  ) + x(i1+1,i2  ,i3  ) ...
                            + x(i1  ,i2+1,i3  ) + x(i1+1,i2+1,i3  ) ...
                            + x(i1  ,i2  ,i3+1) + x(i1+1,i2  ,i3+1) ...
                            + x(i1  ,i2+1,i3+1) + x(i1+1,i2+1,i3+1) )/8;
        GRID.gblock(n).yc = ( y(i1  ,i2  ,i3  ) + y(i1+1,i2  ,i3  ) ...
                            + y(i1  ,i2+1,i3  ) + y(i1+1,i2+1,i3  ) ...
                            + y(i1  ,i2  ,i3+1) + y(i1+1,i2  ,i3+1) ...
                            + y(i1  ,i2+1,i3+1) + y(i1+1,i2+1,i3+1) )/8;
        GRID.gblock(n).zc = ( z(i1  ,i2  ,i3  ) + z(i1+1,i2  ,i3  ) ...
                            + z(i1  ,i2+1,i3  ) + z(i1+1,i2+1,i3  ) ...
                            + z(i1  ,i2  ,i3+1) + z(i1+1,i2  ,i3+1) ...
                            + z(i1  ,i2+1,i3+1) + z(i1+1,i2+1,i3+1) )/8;
    else
        i3 = 1:GRID.gblock(n).kmax;
        GRID.gblock(n).xc = ( x(i1  ,i2  ,i3  ) + x(i1+1,i2  ,i3  ) ...
                            + x(i1  ,i2+1,i3  ) + x(i1+1,i2+1,i3  ) )/4;
        GRID.gblock(n).yc = ( y(i1  ,i2  ,i3  ) + y(i1+1,i2  ,i3  ) ...
                            + y(i1  ,i2+1,i3  ) + y(i1+1,i2+1,i3  ) )/4;
        GRID.gblock(n).zc = ( z(i1  ,i2  ,i3  ) + z(i1+1,i2  ,i3  ) ...
                            + z(i1  ,i2+1,i3  ) + z(i1+1,i2+1,i3  ) )/4;
    end
end
end